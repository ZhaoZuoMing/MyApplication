public class Credential {
    private  String Id;
    private String AccountId;
    private String Type;
    private String TypeName;
    private String Number;
    private String FirstName;
    private String LastName;
    private String CheckName;
    private String CheckFirstName;
    private String CheckLastName;
    private String ExpirationDate;
    private String Country;
    private String IssueCountry;
    private String Birthday;
    private String Gender;

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getAccountId() {
        return AccountId;
    }

    public void setAccountId(String accountId) {
        AccountId = accountId;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getTypeName() {
        return TypeName;
    }

    public void setTypeName(String typeName) {
        TypeName = typeName;
    }

    public String getNumber() {
        return Number;
    }

    public void setNumber(String number) {
        Number = number;
    }

    public String getFirstName() {
        return FirstName;
    }

    public void setFirstName(String firstName) {
        FirstName = firstName;
    }

    public String getLastName() {
        return LastName;
    }

    public void setLastName(String lastName) {
        LastName = lastName;
    }

    public String getCheckName() {
        return CheckName;
    }

    public void setCheckName(String checkName) {
        CheckName = checkName;
    }

    public String getCheckFirstName() {
        return CheckFirstName;
    }

    public void setCheckFirstName(String checkFirstName) {
        CheckFirstName = checkFirstName;
    }

    public String getCheckLastName() {
        return CheckLastName;
    }

    public void setCheckLastName(String checkLastName) {
        CheckLastName = checkLastName;
    }

    public String getExpirationDate() {
        return ExpirationDate;
    }

    public void setExpirationDate(String expirationDate) {
        ExpirationDate = expirationDate;
    }

    public String getCountry() {
        return Country;
    }

    public void setCountry(String country) {
        Country = country;
    }

    public String getIssueCountry() {
        return IssueCountry;
    }

    public void setIssueCountry(String issueCountry) {
        IssueCountry = issueCountry;
    }

    public String getBirthday() {
        return Birthday;
    }

    public void setBirthday(String birthday) {
        Birthday = birthday;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        Gender = gender;
    }
}
